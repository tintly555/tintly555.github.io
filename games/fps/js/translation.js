export function mergeKeys(base, extra) {
  const out = { ...base };
  for (const k of Object.keys(base)) {
    if (extra[k] != null) out[k] = extra[k];
  }
  for (const k of Object.keys(extra)) {
    if (out[k] == null) out[k] = extra[k];
  }
  return out;
}

