// Merge into each language dict. Existing user-translated keys are NOT overwritten — these are
// only added if missing (so manual edits in the dicts above always win).
function _addIfMissing(target, extras) {
  for (const k of Object.keys(extras)) {
    if (target[k] == null || target[k] === "") target[k] = extras[k];
  }
}
// v96: stronger — also replaces values that equal the English placeholder. Used for ach/V96 keys
// where the original translation.txt left English strings ("Delete all data", "What Is This?")
// as filler. Pass the EN dict as the third arg so we know what counts as "placeholder".
function _replaceIfPlaceholder(target, extras, enRef) {
  for (const k of Object.keys(extras)) {
    if (target[k] == null || target[k] === "" || (enRef && target[k] === enRef[k])) {
      target[k] = extras[k];
    }
  }
}
