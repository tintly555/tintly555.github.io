export function translate(lang, key, fallback = "") {
  const code = GAME_I18N[lang] ? lang : "en";
  const dict = GAME_I18N[code] || GAME_I18N.en;
  const en = GAME_I18N.en;
  if (dict[key] != null && dict[key] !== "") return dict[key];
  if (en[key] != null && en[key] !== "") return en[key];
  return fallback;
}
